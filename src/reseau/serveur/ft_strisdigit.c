#include "libft.h"

int	ft_strisdigit(char const *str)
{
	int	i;

	i = -1;
	if (!str)
		return (0);
	while (str[++i])
	{
		if ('0' > str[i] || '9' < str[i])
			return (0);
	}
	return (1);
}
