#include "server.h"

int	command_get_int(int i, char *cmd)
{
	int	ret;

	ret = -1;
	while (cmd && cmd[++i] != '\0' && cmd[i] >= '0' && cmd[i] <= '9')
	{
		if (ret == -1)
			ret = 0;
		ret = (ret * 10) + (cmd[i] - 48);
	}
	return (ret);
}
