#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*dest;
	int		i;

	dest = NULL;
	i = 0;
	dest = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
