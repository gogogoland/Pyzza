#include "libft.h"

int	ft_atoi(const char *s)
{
	int	n;
	int	neg;
	int	i;

	i = -1;
	n = 0;
	if (s == NULL)
		return (n);
	while (((s[++i] >= '\t' && s[i] <= '\r') || s[i] == ' ') && s[i] != '\0')
		;
	neg = (s[i] == '-') ? -1 : 1;
	i -= (s[i] == '+' || s[i] == '-') ? 0 : 1;
	while (ft_isdigit(s[++i]) && s[i] != '\0')
		n = n * 10 + s[i] - '0';
	return (n * neg);
}
