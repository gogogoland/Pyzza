#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = -1;
	if (s1 && s2)
	{
		while (++i < n)
		{
			if (s1[i] != s2[i] || s1[i] == '\0')
				return ((int)s1[i] - (int)s2[i]);
		}
	}
	return (0);
}
